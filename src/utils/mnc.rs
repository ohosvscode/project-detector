  /// Check if the mnc is a valid MNC code with string `mnc` and `mcc`.
  pub fn is_code(mnc: String, mcc: u32) -> bool {
    if !mnc.starts_with("mnc") {
      return false;
    }
    let mnc = mnc.replace("mnc", "");
    match mnc.parse::<u32>() {
      Ok(mnc) => MNC::is(mcc, mnc),
      Err(_) => false,
    }
  }
